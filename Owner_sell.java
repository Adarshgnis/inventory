    private void productid_txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_productid_txtKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            try {
                String id = productid_txt.getText();
                
                
                pst = con.prepareStatement("select * from product_list where id=?");
                pst.setString(1, id);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Product not found");
                    productid_txt.setText("");
                }
                else
                {
                    String productname = rs.getString("name");
                    productname_txt.setText(productname.trim());
                    
                    String price = rs.getString("rate");
                    price_txt.setText(price.trim());
                    
                    
                    
                    qty_txt.requestFocus();
                    
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Owner_sell.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            
    }//GEN-LAST:event_productid_txtKeyPressed


    public void sales()
    {
        String totalcost = totalcost_txt.getText();
        String pay = pay_txt.getText();
        String bal = balance_txt.getText();
        
        int lastid = 0;
        
        
        try {
            String query = "insert into sell(subtotal,pay,balance)values(?,?,?)";
            pst = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, totalcost);
            pst.setString(2, pay);
            pst.setString(3, bal);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                lastid = rs.getInt(1);
            }
            
            int rows = sell_table.getRowCount();
            
            
            String query1 = "insert into sell_product(sell_id,product_name,price,quantity,total_price,date)values(?,?,?,?,?,CURRENT_TIMESTAMP)";
            pst1 = con.prepareStatement(query1);
            
            
            String prodname = "";
            String price;
            String qty;
            int total = 0;
            
            for(int i=0; i<sell_table.getRowCount(); i++)
            {
                prodname = (String)sell_table.getValueAt(i, 0);
                price = (String)sell_table.getValueAt(i, 1);
                qty = (String)sell_table.getValueAt(i, 2);
                total = (int)sell_table.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, prodname);
                pst1.setString(3, price);
                pst1.setString(4, qty);
                pst1.setInt(5, total);
                pst1.executeUpdate();
            }
            JOptionPane.showMessageDialog(this, "Sell Completed");
            
        } catch (SQLException ex) {
            Logger.getLogger(Owner_sell.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    


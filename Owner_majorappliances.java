    private void table_update()
    {
        try {
            int c;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory", "root", "pooja");
            pst = con1.prepareStatement("select * from product_list where category_id = 1");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)majorappliances_table.getModel();
            d.setRowCount(0);
            
            while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1;i<=c;i++)
                    {
                        v2.add(rs.getString("id"));
                        v2.add(rs.getString("name"));
                        v2.add(rs.getString("rate"));
                        v2.add(rs.getString("stock"));
                    }
                    
                    d.addRow(v2);
                }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Owner_majorappliances.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Owner_majorappliances.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    

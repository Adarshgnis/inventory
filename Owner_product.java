        DefaultTableModel d1 = (DefaultTableModel)product_table.getModel();
        int selectIndex = product_table.getSelectedRow();
        
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        String product  = productname_txt.getText();
        CategoryItem categoryitem = (CategoryItem)cat_combo.getSelectedItem();
        DepartmentItem departmentitem = (DepartmentItem)dept_combo.getSelectedItem();

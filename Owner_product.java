     private void table_update()
    {
        try {
            int c;
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory", "root", "pooja");
                pst = con1.prepareStatement("select * from product");
                ResultSet rs = pst.executeQuery();
                
                ResultSetMetaData rsd = rs.getMetaData();
                c = rsd.getColumnCount();
                
                DefaultTableModel d = (DefaultTableModel)product_table.getModel();
                d.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1;i<=c;i++)
                    {
                        v2.add(rs.getString("id"));
                        v2.add(rs.getString("product"));
                        v2.add(rs.getString("category"));
                        v2.add(rs.getString("department"));
                        v2.add(rs.getString("price"));
                        v2.add(rs.getString("quantity"));
                    }
                    
                    d.addRow(v2);
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Owner_product.class.getName()).log(Level.SEVERE, null, ex);
            }     
        } catch (SQLException ex) {
            Logger.getLogger(Owner_product.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    

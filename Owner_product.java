    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
      
       
        String product  = productname_txt.getText();
        CategoryItem categoryitem = (CategoryItem)cat_combo.getSelectedItem();
        DepartmentItem departmentitem = (DepartmentItem)dept_combo.getSelectedItem();
        String price  = price_txt.getText();
        String quantity  = quantity_txt.getText();
        
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory", "root", "pooja");
            pst = con1.prepareStatement("insert into product(product,category,department,price,quantity)values(?,?,?,?,?) ");
            pst.setString(1, product);
            pst.setString(2, categoryitem.name);
            pst.setString(3, departmentitem.name);
            pst.setString(4, price);
            pst.setString(5, quantity);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Product Added");
            table_update();
            
            
            productname_txt.setText("");
            cat_combo.setSelectedIndex(-1);
            dept_combo.setSelectedIndex(-1);
            price_txt.setText("");
            quantity_txt.setText("");
            productname_txt.requestFocus();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Owner_department.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Owner_department.class.getName()).log(Level.SEVERE, null, ex);
        }
       

    }//GEN-LAST:event_add_buttonActionPerformed

    
    
    

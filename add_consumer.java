    private void addpurchaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addpurchaseMouseClicked
        // TODO add_major your handling code here:
        
        String productname = productname_txt.getText();
        String stock = quantity_txt.getText();
        String rate = price_txt.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory", "root", "pooja");
            pst = con1.prepareStatement("insert into product_list (name,category_id,rate,stock)values(?,3,?,?)");
            pst.setString(1, productname);
            pst.setString(2, rate);
            pst.setString(3, stock);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Product Purchased"); 
            
            
            /*productid_txt.setText("");
            productname_txt.setText("");
            quantity_txt.setText("");
            price_txt.setText("");
            productid_txt.requestFocus();*/
            
            setVisible(false);
            
             
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add_consumer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(add_consumer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_addpurchaseMouseClicked

